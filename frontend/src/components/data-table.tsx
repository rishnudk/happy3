// Create a separate component for the drag handle
function DragHandle({ id }: { id: number }) {
  const { attributes, listeners } = useSortable({
    id,
  })

  return (
    <Button
      {...attributes}
      {...listeners}
      variant="ghost"
      size="icon"
      className="size-7 text-muted-foreground hover:bg-transparent"
    >
      <GripVerticalIcon className="size-3 text-muted-foreground" />
      <span className="sr-only">Drag to reorder</span>
    </Button>
  )
}

function StatusIcon({ status }: { status: string }) {
  switch (status) {
    case "Enrolled":
      return <CircleCheckIcon className="fill-green-500 dark:fill-green-400" />
    case "Qualified":
      return <CircleDotIcon className="fill-blue-500 dark:fill-blue-400" />
    case "Contacted":
      return <LoaderIcon />
    case "Lost":
      return <CircleXIcon className="fill-red-500 dark:fill-red-400" />
    default:
      return <CircleIcon className="fill-amber-500 dark:fill-amber-400" />
  }
}

